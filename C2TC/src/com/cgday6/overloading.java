package com.cgday6;

public class overloading {
	public static int add(int a,int b) {
		return a+b;
	}
	public static int add(int a,int b,int c) {
		return a+b+c;
	}
	public static float add(float a,int b) {
		return a+b;
	}
	public static float add(float a,float b) {
		return a+b;
	}

}
